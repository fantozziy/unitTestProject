public class Calculator {
    public int getSum(int firstX, int secondX) {
        return firstX + secondX;
    }

    public int getMinus(int firstX, int secondx) {
        return firstX - secondx;
    }

    public int getQuotient(int firstX, int secondX) {
        return firstX / secondX;
    }
}
